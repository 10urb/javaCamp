package Entity;

import Core.IEntity;

public class Campaign implements IEntity{
	
	int id;
	String name;
	
	
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
